package com.suraj;

public class Main {

    public static void main(String[] args) {
	    String message = "Hello \"Suraj\""; //to use double quotes use \
        System.out.println(message);

        String message2 = "c:\\Windows\\...";  //to insert one backslash use two backlash
        System.out.println(message2);

        String message3 = "c:\nWindows\\...";  //to insert new line
        System.out.println(message3);

        String message4 = "c:\tWindows\\...";  //to insert new tab
        System.out.println(message3);
    }
}
